
public class Staticvariables {

	static String sum="sum:";//static variable declaration
	static String diff ="difference:";
	
	   public static void main(String[] args) {
		
		   int a=10;
		   int b=20;
		   int c=a+b;
		   int d=a-b;

		   System.out.println(sum+c);
		   System.out.println(diff+d);

	}

}
