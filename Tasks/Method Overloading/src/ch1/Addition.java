package ch1;

public class Addition {
	// adding three integer values.
    public int add(int a, int b, int c)
    {
 
        int sum = a + b + c;
        return sum;
    }
 
    // adding three double values.
    public double add(double a, double b, double c)
    {
 
        double sum = a + b + c;
        return sum;
    }
}

